int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    if (!(cin >> t)) return 0;
    while (t--) {
        long long n;
        cin >> n;

        string str;
        cin >> str;

        int ans = 0;

        char ch = str[str.size() - 1];
        for (int i = 0; i < n; i++) {
            if (str[i] != ch) {
                ans++;
            }
        }

        cout << ans << endl;
    }
    return 0;
}